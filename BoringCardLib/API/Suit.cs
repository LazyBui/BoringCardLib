using System;

namespace BoringCardLib {
	/// <summary>
	/// Represents the suit of a playing card.
	/// </summary>
	public enum Suit {
		/// <summary>
		/// Represents the spades suit.
		/// </summary>
		Spades,
		/// <summary>
		/// Represents the clubs suit.
		/// </summary>
		Clubs,
		/// <summary>
		/// Represents the hearts suit.
		/// </summary>
		Hearts,
		/// <summary>
		/// Represents the diamonds suit.
		/// </summary>
		Diamonds,
		/// <summary>
		/// Represents a joker.
		/// </summary>
		Joker,
	}
}